# In the first part of the code, tup = 'Hello', the variable tup is assigned the string 'Hello'. When the len()
# function is called, the length of the string is returned.

tup = 'Hello'
print(len(tup))
variable_type = type(tup)
print(variable_type)

# "tup = 'Hello'," the variable tup is assigned a tuple containing a single element,
# which is the string 'Hello'. When the len() function is called on tup, it returns 1 as there is one
# element in the tuple.

tup = 'Hello',
print(len(tup))
variable_type = type(tup)
print(variable_type)
